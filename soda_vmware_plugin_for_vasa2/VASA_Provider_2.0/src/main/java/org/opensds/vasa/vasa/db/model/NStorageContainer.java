package org.opensds.vasa.vasa.db.model;

public class NStorageContainer extends BaseData {

    private String containerId;
    private String containerName;
    private String description;
    private String status;
    private String containerType;

    @Override
    public String toString() {
        return "NStorageContainer [containerId=" + containerId
                + ", containerName=" + containerName + ", description="
                + description + ", status=" + status + ", containerType="
                + containerType + "]";
    }

    public String getContainerId() {
        return containerId;
    }

    public void setContainerId(String containerId) {
        this.containerId = containerId;
    }

    public String getContainerName() {
        return containerName;
    }

    public void setContainerName(String containerName) {
        this.containerName = containerName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getContainerType() {
        return containerType;
    }

    public void setContainerType(String containerType) {
        this.containerType = containerType;
    }
}
