package org.opensds.vasa.vasa.db.model;

public class EsxHostIp {

    private String id;
    private String ip;
    private String hostId;

    public EsxHostIp() {
        super();
    }

    public EsxHostIp(String ip, String hostId) {
        super();
        this.ip = ip;
        this.hostId = hostId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getHostId() {
        return hostId;
    }

    public void setHostId(String hostId) {
        this.hostId = hostId;
    }

    @Override
    public String toString() {
        return "EsxHostIp [id=" + id + ", ip=" + ip + ", hostId=" + hostId + "]";
    }

}
