package org.opensds.vasa.vasa20.device.array.bean;

public class DeviceUserAuthResBean {

    private String deviceid;

    private String iBaseToken;

    private String username;

    private String userid;

    public String getDeviceid() {
        return deviceid;
    }

    public void setDeviceid(String deviceid) {
        this.deviceid = deviceid;
    }

    public String getiBaseToken() {
        return iBaseToken;
    }

    public void setiBaseToken(String iBaseToken) {
        this.iBaseToken = iBaseToken;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    @Override
    public String toString() {
        return "DeviceUserAuthResBean [deviceid=" + deviceid + ", iBaseToken="
                + iBaseToken + ", username=" + username + ", userid=" + userid
                + "]";
    }


}
